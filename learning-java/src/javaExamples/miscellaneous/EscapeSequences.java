package javaExamples.miscellaneous;

// Program to use Escape Sequences

class EscapeSequences {
	public static void main(String args[]) {
		System.out.print("tLoreum Ipsum");
		System.out.print("nLoreum Ipsum");
		System.out.print("nLoerum Ipsum");
		System.out.print("nLoreum Ipsumr");
		System.out.print("'Loreum Ipsum'");
		System.out.print("n" + "Loreum Ipsum");
		System.out.print("nfLoreum Ipsum");
		System.out.print("nLoreum Ipsum");
	}
}